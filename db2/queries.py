"""
Шаг 3: Выполнение всех запросов к базе данных
Запускать ТРЕТЬИМ — выводит результаты всех 8 запросов
"""

import sqlite3

conn = sqlite3.connect("students.db")
cur = conn.cursor()


# ЗАПРОС 1: Общее количество студентов
print("Запрос 1: Количество всех студентов")

cur.execute("""
    SELECT COUNT(*) AS всего_студентов
    FROM студенты
""")

for row in cur.fetchall():
    print(row)



# ЗАПРОС 2: Количество студентов по направлениям
print("\nЗапрос 2: Количество студентов по направлениям")

cur.execute("""
    SELECT
        н.название      AS направление,
        COUNT(*)        AS количество_студентов
    FROM студенты с
    JOIN направления н ON с.id_направления = н.id_направления
    GROUP BY н.название
    ORDER BY количество_студентов DESC
""")

for row in cur.fetchall():
    print(row)


# ЗАПРОС 3: Количество студентов по формам обучения
print("\nЗапрос 3: Количество студентов по формам обучения")

cur.execute("""
    SELECT
        т.название   AS форма_обучения,
        COUNT(*)     AS количество_студентов
    FROM студенты с
    JOIN типы_обучения т ON с.id_типа_обучения = т.id_типа
    GROUP BY т.название
    ORDER BY количество_студентов DESC
""")

for row in cur.fetchall():
    print(row)


# ЗАПРОС 4: Максимальный, минимальный, средний балл по направлениям
print("\nЗапрос 4: Макс / мин / средний балл по направлениям")

cur.execute("""
    SELECT
        н.название                    AS направление,
        MAX(с.средний_балл)           AS максимальный,
        MIN(с.средний_балл)           AS минимальный,
        ROUND(AVG(с.средний_балл), 1) AS средний
    FROM студенты с
    JOIN направления н ON с.id_направления = н.id_направления
    GROUP BY н.название
    ORDER BY средний DESC
""")

for row in cur.fetchall():
    print(row)


# ЗАПРОС 5: Средний балл по направлению + уровню + форме обучения
print("\nЗапрос 5: Средний балл по направлению, уровню и форме обучения")

cur.execute("""
    SELECT
        н.название                    AS направление,
        у.название                    AS уровень,
        т.название                    AS форма_обучения,
        COUNT(*)                      AS студентов,
        ROUND(AVG(с.средний_балл), 1) AS средний_балл
    FROM студенты с
    JOIN направления      н ON с.id_направления   = н.id_направления
    JOIN уровень_обучения у ON с.id_уровня         = у.id_уровня
    JOIN типы_обучения    т ON с.id_типа_обучения  = т.id_типа
    GROUP BY н.название, у.название, т.название
    ORDER BY направление, уровень, форма_обучения
""")

for row in cur.fetchall():
    print(row)


# ЗАПРОС 6: Топ-5 студентов «Прикладной Информатики» очной формы
print("\nЗапрос 6: Топ-5 на повышенную стипендию (Прикладная Информатика, очная)")

cur.execute("""
    SELECT
        с.фамилия,
        с.имя,
        с.отчество,
        с.средний_балл
    FROM студенты с
    JOIN направления   н ON с.id_направления   = н.id_направления
    JOIN типы_обучения т ON с.id_типа_обучения  = т.id_типа
    WHERE н.название = 'Прикладная Информатика'
      AND т.название = 'Очная'
    ORDER BY с.средний_балл DESC
    LIMIT 5
""")

for row in cur.fetchall():
    print(row)


# ЗАПРОС 7: Однофамильцы
print("\nЗапрос 7: Однофамильцы")

cur.execute("""
    SELECT
        фамилия,
        COUNT(*) AS количество
    FROM студенты
    GROUP BY фамилия
    HAVING COUNT(*) > 1
    ORDER BY количество DESC
""")

for row in cur.fetchall():
    print(row)


# ЗАПРОС 8: Полные тёзки (совпадают фамилия + имя + отчество)
print("\nЗапрос 8: Полные тёзки")

cur.execute("""
    SELECT
        фамилия,
        имя,
        отчество,
        COUNT(*) AS количество
    FROM студенты
    GROUP BY фамилия, имя, отчество
    HAVING COUNT(*) > 1
    ORDER BY количество DESC
""")

rows = cur.fetchall()
if rows:
    for row in rows:
        print(row)
else:
    print("Полных тёзок не найдено")


conn.close()